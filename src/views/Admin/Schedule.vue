<template>
  <div>
    <Title title="Schedule" subtitle="View your calendar" />
    <el-row v-loading="loading" type="flex">
      <el-col class="pl-3 pr-3">
        <el-row class="mb-4" type="flex" :gutter="10">
          <el-col>
            <el-input placeholder="Search" />
          </el-col>
          <el-col>
            <Dropdown
              :items="items"
              @method="displayModals"
              :icon="false"
              position="right"
            >
              <el-button round type="primary"
                >Actions <i class="el-icon-arrow-right "></i
              ></el-button>
            </Dropdown>
          </el-col>
        </el-row>
        <ScheduleTable />
      </el-col>
    </el-row>

    <ScheduleFormDialog
      @toggle="modals.createEvent = $event"
      @createEvent="createEvent"
      :display="modals.createEvent"
    />
    <ScheduleProfileView />
  </div>
</template>

<script>
import VueCal from 'vue-cal'
import 'vue-cal/dist/vuecal.css'
import dates from '@/mixins/dates'
import { mapState, mapActions, mapGetters } from 'vuex'
import ScheduleFormDialog from './components/ScheduleFormDialog'
import Dropdown from '@/components/Dropdown.vue'
import Popover from '@/components/Popover'
import ScheduleProfileView from './components/ScheduleProfileView.vue'

export default {
  name: 'Schedule',
  data() {
    return {
      loading: false,
      filters: {
        employee: '',
        abscences: '',
        holidays: '',
        late: ''
      },
      modals: {
        createEvent: false,
        editEvent: false,
        viewProfile: false
      },

      currentView: '',
      dateFormat: 'DD MMMM'
    }
  },
  created() {
    // this.getTeam()
    this.getTeam()
    this.getShifts()
  },

  computed: {
    ...mapState('Admin', ['team']),
    ...mapGetters(['getIsAdmin']),
    items() {
      const isAdmin = this.getIsAdmin
      let items = [
        {
          name: isAdmin ? 'Create Event' : 'Create Request',
          command: 'add_event'
        },

        {
          name: isAdmin ? 'Update / Remove Event' : 'Update / Remove Request',
          command: 'edit_event'
        },
        {
          name: 'Create Employee',
          command: 'create_employee',
          divided: true
        },
        {
          name: 'Export Employee',
          command: 'export_employee'
        },
        {
          name: 'Export Schedule',
          command: 'export_schedule'
        }
      ]

      if (!isAdmin) {
        items.filter(item => {
          return (
            item.command == 'export_employee' ||
            item.command == 'create_employee'
          )
        })
      }
      return items
    },

    viewSelectionConfig() {
      return [
        { name: 'Month', value: 'month' },
        { name: 'Week', value: 'week' },
        { name: 'Day', value: 'day' }
      ]
    },
    filterConfig() {
      let filters = []
      for (let filter in this.filters) {
        filters.push(filter)
      }
      return filters
    },
    formConfig() {
      return [
        {
          label: 'Assign To and Date',
          items: { name: '', model: '', component: '' }
        }
      ]
    }
  },
  methods: {
    ...mapActions(['request']),
    ...mapActions('Admin', ['getTeam', 'getShifts']),
    createEvent(eventData) {
      this.loading = true
      this.modals.createEvent = false
      const date = { start: eventData.date[0], end: eventData.date[1] }
      const completeStartDate = this.toISO(date.start)
      const completeEndDate = this.toISO(date.end)

      const payload = {
        url: '/shifts/create',
        method: 'POST',
        data: {
          startDate: completeStartDate,
          endDate: completeEndDate,
          shift_type: eventData.eventType
        }
      }
      if (eventData.assignTo) {
        payload.assigned_to = eventData.assignTo
      }
      this.request(payload)
        .then(response => {
          const message = this.getIsAdmin
            ? 'Event successfully created'
            : 'Request successfully created'
          this.$notify({
            title: 'Success',
            message: message,
            type: 'success'
          })
          this.loading = false
        })
        .catch(error => {
          this.$notify.error({
            title: 'Error',
            message: 'Error when creating event, please try again later'
          })
          this.loading = false
        })
    },
    displayModals(command) {
      if (command == 'add_event') {
        this.modals.createEvent = true
      } else {
        this.modals.editEvent = true
      }
    }
  },
  mixins: [dates],

  components: {
    VueCal,
    Title: () => import('@/components/Title'),
    ScheduleTable: () => import('./components/ScheduleTable'),
    ScheduleFormDialog,
    Dropdown,
    Popover,
    ScheduleProfileView
  }
}
</script>

